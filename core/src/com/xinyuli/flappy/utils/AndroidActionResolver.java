package com.xinyuli.flappy.utils;

/**
 * Created by xinyuli on 25/04/2017.
 *
 * Android code passer.
 */
public interface AndroidActionResolver {
    /**
     * Show a toast with the corresponding text with LENGTH.SHORT.
     * @param text The text goes in the toast.
     */
    void showToast(CharSequence text);

    /**
     * @return Current volume from the recorder of android device.
     */
    int getVolume();

    /**
     * Let user signIn into google play services.
     */
    void signIn();

    /**
     * Submit the high score to google play leader board.
     */
    void submitScore(int highScore);

    /**
     * Shows the user google play ui for checking global high scores.
     */
    void showScore();

    /**
     * @return If the current user is signed in to google play.
     */
    boolean isSignedIn();
}
